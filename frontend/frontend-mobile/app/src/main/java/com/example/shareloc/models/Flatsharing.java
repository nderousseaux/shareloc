package com.example.shareloc.models;

public class Flatsharing {

    private int id;
    private String name;

    public Flatsharing (int id, String name) {
        this.id = (id);
        this.name = (name);
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
